package apiclient;

/**
 * RSSClientException is a custom exception class for the RSS client
 * 
 */
public class RSSClientException extends Exception {

   public RSSClientException() {
      super();
   }

   public RSSClientException(String message) {
      super(message);
   }

   public RSSClientException(String message, Throwable cause) {
      super(message, cause);
   }
   
   

}
